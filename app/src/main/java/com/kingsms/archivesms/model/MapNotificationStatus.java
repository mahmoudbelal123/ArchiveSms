package com.kingsms.archivesms.model;

public class MapNotificationStatus {

    private String mapSenderName;
    private int mapStatus;


    public String getMapSenderName() {
        return mapSenderName;
    }

    public void setMapSenderName(String mapSenderName) {
        this.mapSenderName = mapSenderName;
    }

    public int getMapStatus() {
        return mapStatus;
    }

    public void setMapStatus(int mapStatus) {
        this.mapStatus = mapStatus;
    }
}
